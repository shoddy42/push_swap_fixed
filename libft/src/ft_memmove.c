#include "../libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (!dst && !src)
		return (NULL);
	if (dst < src)
		ft_memcpy(dst, src, n);
	else
	{
		while (i < n)
		{
			i++;
			((unsigned char *)dst)[n - i] = ((unsigned char *)src)[n - i];
		}
	}
	return (dst);
}
